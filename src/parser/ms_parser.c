#include "../../header/minishell.h"

int	ms_parser(t_shell *shell)
{
	shell->cmd->args = ft_split(shell->cmd->line, ' ');
	if (shell->cmd->args == NULL)
	{
		shell->errnum = 3;
		return (1);
	}
	return (0);
}
